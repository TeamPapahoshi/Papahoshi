//=======================================================================================
void cBG::SetBG(BG bg){

	type = bg;

	// 背景は同じものを複数ロードしないのでここで直接ロードします
	switch (type)
	{
	case TITLE:
		break;
	case GAME_MAIN:
		break;
	case GAME_SKY:
		sprite.LoadTexture(BG_FILNAME_SKY);
		break;
	case RESULT:
		break;
	default:
		break;
	}


}

