//=======================================================================================
//
//		星が見えるか見えないかの設定
//
//=======================================================================================
void cNormalStar::StarVisibility(float distance){

	// α値の変化
	if (distance != 0){
		m_sprite.SetVtxColorA(255 / distance * 20);
	}
	else{
		m_sprite.SetVtxColorA(255);
	}
}

