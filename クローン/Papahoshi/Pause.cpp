cPause::~cPause(){

}

//=======================================================================================
//
//		初期化
//
//=======================================================================================
void cPause::Init(){
	//動的確保したクラスの初期化
	cpPauseScreen.SetPos(D3DXVECTOR2((float)SCREEN_WIDTH / 2, (float)SCREEN_HEIGHT / 2));
	cpPauseScreen.SetSize(D3DXVECTOR2(SCREEN_WIDTH, SCREEN_HEIGHT));
	cpPauseScreen.SetVtxColorA(60);

	//フラグの初期化
	bPauseFlag = false;
}

//=======================================================================================
//
//		終了
//
//=======================================================================================
void cPause::Uninit(){


}

//=======================================================================================
//
//		更新
//
//=======================================================================================
void cPause::Update(){
	//Pキーが入力されるごとに状態切り替え
	if (GetKeyboardTrigger(DIK_P))
	{
		bPauseFlag ^= 1;
	}
}

//=======================================================================================
//
//		描画
//
//=======================================================================================
void cPause::Draw(){
	//フラグが立っている場合のみ描画
	if (bPauseFlag)
	{
		cpPauseScreen.Draw();
	}
}

//=======================================================================================
//
//		ポーズフラグの取得
//
//=======================================================================================
bool cPause::GetPauseFlag(void)
{
	return bPauseFlag;
}